#similar to list but tuples are immutable ie they cannot be changed
t=(4,5,8)
print(t)

#indexing
print(t[0])

# to count the length use len
print(len(t))

# basic tuples methods
d=(4,5,6,7,8,4,3)
#use .index to enter a value and return index
print(d.index(6))

#use .count  to count the number of times a value appeares
print(d.count(4))

